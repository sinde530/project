class Calculator:
  def input_num(self, first, second):
    self.first = first
    self.second = second

  def plus(self):
    return self.first + self.second

  def minus(self):
    return self.first - self.second

  def mul(self):
    return self.first * self.second
    
  def div(self):
    return self.first / self.second
